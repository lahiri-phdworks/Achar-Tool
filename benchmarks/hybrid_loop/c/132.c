int main() {
    // variable declarations
    int i;
    int j;
    int c;
    int t;
    // pre-conditions
    // scanf("%d", &i);
    // scanf("%d", &j);
    // scanf("%d", &c);
    // scanf("%d", &t);
    (i = 0);
    // precheck
    // loopcond : unknown()
    // loopstart
    while (unknown()) {
        // loop body
        if (c > 48) {
            if (c < 57) {
                // j = i + i;
                assume((48 == 0));
                t = c - 48;
                i = j + t;
            }
        }
    }
    // loopend
    // postcheck
    // post-condition
    assert(i >= 0);
}